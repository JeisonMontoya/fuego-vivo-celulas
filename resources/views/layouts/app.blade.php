<script>
    // Parche para Livewire SPA:
    // Al navegar entre páginas sin recargar (wire:navigate), window.Echo mantiene las suscripciones vivas.
    // Esto rompe los eventos "here()" de los canales de presencia en la nueva página.
    // Solución: Desconectarse del canal justo antes de cambiar de página para forzar una reconexión limpia.
    document.addEventListener('livewire:navigating', () => {
        if (window.Echo) {
            window.Echo.leave('general-chat');
        }
    });
</script>
